public class Main {
    public static void main(String[] args) {

        //Tipos de datos

        //1.1 Enteros
        byte num = 127;
        System.out.println(num);
        short num1 = 32502;
        System.out.println(num1);
        int num2 = 4203912;
        System.out.println(num2);
        long num3 = 556712398;
        System.out.println(num3);

        //1.2 Decimales
        float num4 = 1.234f;
        System.out.println(num4);
        double num5 = 1.23567;
        System.out.println(num5);

        //1.3 Booleanos
        boolean cielo = true;
        System.out.println(cielo);
        boolean marSeco = false;
        System.out.println(marSeco);

        //1.4 Strings
        char caracter = 'a';
        System.out.println(caracter);
        String fin = "Final";
        System.out.println(fin);

    }
}